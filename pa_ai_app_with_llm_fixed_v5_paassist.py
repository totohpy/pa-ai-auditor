def read_pdf_text(uploaded_file):
    """Extracts text content from a PDF file."""
    try:
        reader = PdfReader(uploaded_file)
        text = ""
        for page in reader.pages:
            text += page.extract_text() or ""
        return text
    except Exception as e:
        st.error(f"‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏≠‡πà‡∏≤‡∏ô PDF: {e}")
        return ""


def handle_document_upload(uploaded_files, max_chars=100000):
    """Processes uploaded files and extracts text context, with character limit."""
    context = ""
    total_chars = 0
    
    for uploaded_file in uploaded_files:
        file_extension = os.path.splitext(uploaded_file.name)[1].lower()
        current_text = ""
        
        if file_extension == '.pdf':
            current_text = read_pdf_text(uploaded_file)
        elif file_extension in ['.txt', '.csv']:
            try:
                # To read file as string, use StringIO 
                stringio = StringIO(uploaded_file.getvalue().decode("utf-8"))
                current_text = stringio.read()
            except Exception as e:
                st.error(f"‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏≠‡πà‡∏≤‡∏ô‡πÑ‡∏ü‡∏•‡πå {file_extension}: {e}")
                continue
        else:
            st.warning(f"‡πÑ‡∏ü‡∏•‡πå‡∏ô‡∏≤‡∏°‡∏™‡∏Å‡∏∏‡∏• '{file_extension}' ‡πÑ‡∏°‡πà‡∏£‡∏≠‡∏á‡∏£‡∏±‡∏ö‡∏Å‡∏≤‡∏£‡∏≠‡πà‡∏≤‡∏ô")
            continue
            
        if current_text:
            if total_chars + len(current_text) > max_chars:
                # Truncate if adding the current document exceeds max_chars
                remaining_chars = max_chars - total_chars
                if remaining_chars > 0:
                    context += current_text[:remaining_chars] + f"\n... [‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ñ‡∏π‡∏Å‡∏ï‡∏±‡∏î‡∏ó‡∏≠‡∏ô‡∏à‡∏≤‡∏Å {uploaded_file.name}]"
                st.warning(f"‡∏à‡∏≥‡∏Å‡∏±‡∏î‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ó‡∏µ‡πà‡∏ô‡∏≥‡πÄ‡∏Ç‡πâ‡∏≤‡∏ó‡∏µ‡πà {max_chars} ‡∏ï‡∏±‡∏ß‡∏≠‡∏±‡∏Å‡∏©‡∏£ ‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ó‡∏µ‡πà‡πÄ‡∏Å‡∏¥‡∏ô‡∏à‡∏∞‡∏ñ‡∏π‡∏Å‡∏ï‡∏±‡∏î‡∏≠‡∏≠‡∏Å")
                break
            else:
                context += f"\n--- Start of Document: {uploaded_file.name} ---\n"
                context += current_text
                context += f"\n--- End of Document: {uploaded_file.name} ---\n"
                total_chars += len(current_text)
                st.success(f"‡πÇ‡∏´‡∏•‡∏î‡πÄ‡∏≠‡∏Å‡∏™‡∏≤‡∏£ '{uploaded_file.name}' ({len(current_text):,} ‡∏ï‡∏±‡∏ß‡∏≠‡∏±‡∏Å‡∏©‡∏£)")

    return context


